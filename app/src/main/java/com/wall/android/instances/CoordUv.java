package com.wall.android.instances;

public class CoordUv {

    private float mCoordX;
    private float mCoordY;

    public CoordUv(float coordX, float coordY) {
        mCoordX = coordX;
        mCoordY = coordY;
    }

    public float getX() {
        return mCoordX;
    }

    public float getY() {
        return mCoordY;
    }

}
